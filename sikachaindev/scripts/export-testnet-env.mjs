#!/usr/bin/env node
/**
 * Emit wallet production env vars from testnet URLs (stdout or file).
 *
 * Usage:
 *   TESTNET_CHAIN_ID=abc... \
 *   TESTNET_RPC_URL=https://rpc.testnet.sikachain.gh \
 *   TESTNET_HYPERION_URL=https://hyperion.testnet.sikachain.gh \
 *   TESTNET_APP_URL=https://app.sikachain.gh \
 *   node export-testnet-env.mjs [output-path]
 */
import { writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const out = process.argv[2];

const chainId = process.env.TESTNET_CHAIN_ID ?? process.env.EXPECT_CHAIN_ID;
const rpc = process.env.TESTNET_RPC_URL ?? process.env.NODE_URL;
const hyperion = process.env.TESTNET_HYPERION_URL ?? process.env.HYPERION_URL;
const appUrl = process.env.TESTNET_APP_URL ?? "https://app.sikachain.gh";
const siteUrl = process.env.TESTNET_SITE_URL ?? "https://sikachain.com";
const chainName = process.env.TESTNET_CHAIN_NAME ?? "SikaChain Testnet";

if (!chainId || chainId.includes("REPLACE")) {
  console.error("error: set TESTNET_CHAIN_ID (from get_info after genesis)");
  process.exit(1);
}
if (!rpc) {
  console.error("error: set TESTNET_RPC_URL");
  process.exit(1);
}

const lines = [
  "# Generated by sikachaindev/scripts/export-testnet-env.mjs — paste into hosting provider",
  "",
  "NEXT_PUBLIC_APP_NAME=Sika App",
  "NEXT_PUBLIC_MARKET=gh",
  "NEXT_PUBLIC_WALLET_ROLLOUT=gh-v1",
  `NEXT_PUBLIC_BASE_URL=${appUrl}`,
  "",
  `NEXT_PUBLIC_CHAIN_READ_RPC_URLS=${rpc}`,
  `NEXT_PUBLIC_CHAIN_ID=${chainId}`,
  `NEXT_PUBLIC_CHAIN_NAME=${chainName}`,
  "",
  "NEXT_PUBLIC_CONTRACT_ACCOUNT=sika",
  "NEXT_PUBLIC_TABLE_ACCOUNT=sika",
  "NEXT_PUBLIC_SYSTEM_CONTRACT_DISPLAY_NAME=SikaChain System",
  "NEXT_PUBLIC_TOKEN_CONTRACT=sika.token",
  "NEXT_PUBLIC_REX_CONTRACT=sika.rex",
  "NEXT_PUBLIC_MSIG_CONTRACT=sika.msig",
  "NEXT_PUBLIC_ATOMICASSETS_CONTRACT=atomicassets",
  "NEXT_PUBLIC_TOKEN_SYMBOL=SIKA",
  "NEXT_PUBLIC_TOKEN_PRECISION=4",
  "NEXT_PUBLIC_BP_AVAILABLE_CHAIN=sikachain",
  "NEXT_PUBLIC_STABLECOIN_CONTRACT=sika.token",
  "NEXT_PUBLIC_STABLECOIN_SYMBOL=CGHS",
  "NEXT_PUBLIC_STABLECOIN_PRECISION=4",
  "",
  `NEXT_PUBLIC_WEBSITE_URL=${siteUrl}`,
  "NEXT_PUBLIC_WEBSITE_LOCALE=en",
  hyperion ? `NEXT_PUBLIC_EXPLORER_HYPERION_URL=${hyperion.replace(/\/$/, "")}` : "# NEXT_PUBLIC_EXPLORER_HYPERION_URL=https://hyperion.testnet.sikachain.gh",
  "",
  "# Never on production:",
  "# NEXT_PUBLIC_DEV_WALLET=1",
  "",
];

const body = lines.join("\n") + "\n";
if (out) {
  writeFileSync(out, body, "utf8");
  console.log("Wrote", out);
} else {
  process.stdout.write(body);
}
